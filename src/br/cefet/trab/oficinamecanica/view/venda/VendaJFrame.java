    private void botaoObsoletoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoObsoletoActionPerformed
        // Bot√£o responsavel por excluir a venda
        int linha = jTable1.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
        int cod = getCodVendaFromTable();

        int opt = JOptionPane.showConfirmDialog(this, "Quer realmente tornar obsoleto a Venda de c√≥digo " + cod + "?");
        if (opt == JOptionPane.OK_OPTION) {
            try {

                VendaDao dao = new VendaDao();
                Venda venda = dao.consultarPorCod(cod);
                venda.setObsoleto(true);
                dao.Alterar(venda);

                tModel.removeRow(linha);
                JOptionPane.showMessageDialog(rootPane, "feito com sucesso");
                desligarbotoes();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao tonar obsoleto venda :- " + e.getMessage(), "Erro de altera√ß√£o", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_botaoObsoletoActionPerformed

    private void botaoFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFinalizarActionPerformed
        // Bot√£o responsavel por finalizar a venda

        int linha = jTable1.getSelectedRow();
        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
        int cod = getCodVendaFromTable();
        VendaDao vendaDao = new VendaDao();
        try {
            this.venda = vendaDao.consultarPorCod(cod);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao finalizar venda :- " + e.getMessage(), "Erro de Consulta", JOptionPane.ERROR_MESSAGE);
        }
        if (this.venda.getSituacao().equalsIgnoreCase("N√£o Pago")) {
            this.venda.setSituacao("Pago");
            try {
                vendaDao.Alterar(this.venda);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao finalizar venda :- " + e.getMessage(), "Erro de altera√ß√£o", JOptionPane.ERROR_MESSAGE);
            }
            this.venda = null;
            mandarpreencher();
            desligarbotoes();
            JOptionPane.showMessageDialog(rootPane, "Pago com sucesso, gerando recibo");
        } else {
            JOptionPane.showMessageDialog(rootPane, "J√° est√° pago, gerando recibo");
        }
        
    }//GEN-LAST:event_botaoFinalizarActionPerformed
    public int getCodVendaFromTable() {
        int linha = jTable1.getSelectedRow();

        DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();

        String codStr = tModel.getValueAt(linha, 0).toString();
        int cod = Integer.parseInt(codStr);
        return cod;
    }

    private void preencherJTable(List<Venda> sList) {
        String[] colunas = new String[]{"ID", "Nome", "Pre√ßo-Total", "Situa√ß√£o", "Data-Venda"};
        String[][] dados = new String[sList.size()][5];
        for (int i = 0; i < sList.size(); i++) {
            Venda s = sList.get(i);
            if (s.isObsoleto() != true) {
                dados[i][0] = s.getCdVenda() + "";
                dados[i][1] = s.getNome();
                String valorstr = s.getPrecoTotal()+"";          
            valorstr = valorstr.replace(".", ",");
            String [] valorseparado = valorstr.split(",");
            if(valorseparado[1].length() == 1){
                valorseparado[1] = valorseparado[1]+"0";
            }
            valorstr = valorseparado[0] +","+ valorseparado[1];
                dados[i][2] = valorstr;
                dados[i][3] = s.getSituacao();
                dados[i][4] = s.getDatavenda() + "";
            }
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));
        desligarbotoes();
    }


