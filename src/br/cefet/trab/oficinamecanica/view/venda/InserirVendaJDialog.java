    private void preencherJTable1(List<Servico> sList) {
        String[] colunas = new String[]{"ID", "DescriÃ§Ã£o", "PreÃ§o", "Excluir"};
        String[][] dados = new String[sList.size()][3];
        for (int i = 0; i < sList.size(); i++) {
            Servico s = sList.get(i);
            dados[i][0] = s.getCdServico() + "";
            String valorstr = s.getValor() + "";
            valorstr = valorstr.replace(".", ",");
            String[] valorseparado = valorstr.split(",");
            if (valorseparado[1].length() == 1) {
                valorseparado[1] = valorseparado[1] + "0";
            }
            valorstr = valorseparado[0] + "," + valorseparado[1];
            dados[i][2] = valorstr;
            dados[i][1] = s.getDescricao();
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));
        try {
            botao = new ButtonColumn(this.jTable1, 3, sList, this, null);
        } catch (IOException ex) {
            Logger.getLogger(InserirVendaJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

