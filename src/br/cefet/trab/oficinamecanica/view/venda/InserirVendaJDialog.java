    public void atualizarTotal(Double preco) {
        String repreco = preco + "";
        repreco = repreco.replace(".", ",");

        String[] valorseparado = repreco.split(",");
        if (valorseparado[1].length() == 1) {
            valorseparado[1] = valorseparado[1] + "0";
        }
        repreco = valorseparado[0] + "," + valorseparado[1];
        if (valorseparado[1].length() == 3) {
            repreco = repreco.substring(0, repreco.length() - 1);
        }

        this.precoTotal.setText(repreco);
    }

    public Double getPrecoTotal() {
        String preco = this.precoTotal.getText();
        preco = preco.replace(",", ".");
        Double precoTotal = Double.parseDouble(preco);
        return precoTotal;
    }
