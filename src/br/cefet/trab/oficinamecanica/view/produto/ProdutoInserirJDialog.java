    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Produto s = new Produto();
        if (jTextField2.getText().equalsIgnoreCase("") || jTextField4.getText().equalsIgnoreCase("")
                || jTextField5.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
        } else {
            s.setNome(jTextField2.getText());
            String precostr = jTextField4.getText();
            precostr = precostr.replace(",", ".");
            s.setPreco(Double.parseDouble(precostr));
            s.setQuantidade(Integer.parseInt(jTextField5.getText()));
            s.setObsoleto(false);
            ProdutoDao dao = new ProdutoDao();
            try {
                int codProduto = dao.inserirProduto(s);
                s.setCdproduto(codProduto);

                this.produto = s;

                JOptionPane.showMessageDialog(this, "Produto inserida com sucesso.");

                this.setVisible(false);
                this.dispose();
            } catch (DaoException e) {
                JOptionPane.showMessageDialog(this, "Erro ao inserir Produto :- " + e.getMessage(), "Erro de inclus√£o", JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(ProdutoInserirJDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

