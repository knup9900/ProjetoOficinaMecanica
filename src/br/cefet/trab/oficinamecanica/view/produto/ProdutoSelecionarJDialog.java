    private void preencherJTable(List<Produto> pList) {
        String[] colunas = new String[]{"Cod.", "Nome", "PreÃ§o", "Quantidade"};

        String[][] dados = new String[pList.size()][4];
        for (int i = 0; i < pList.size(); i++) {
            Produto p = pList.get(i);
            dados[i][0] = p.getCdproduto() + "";
            dados[i][1] = p.getNome();
            dados[i][2] = Double.toString(p.getPreco());
            dados[i][3] = p.getQuantidade() + "";
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));

        jButton3.setEnabled(false);
    }
