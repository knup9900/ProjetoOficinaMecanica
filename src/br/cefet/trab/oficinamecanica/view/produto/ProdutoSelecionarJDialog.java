    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean permisao = true;
       try {
             int qtds = Integer.parseInt(jTextField2.getText());

            int linha = jTable1.getSelectedRow();
            DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
            int cod = getCodProdutoFromTable();

            try {
                ProdutoDao pDao = new ProdutoDao();
                this.produto = pDao.consultarPorCod(cod);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao consultar Produto com cod (" + cod + "):-" + ex.getMessage());
            }
            if (qtds > this.produto.getQuantidade()) {
                JOptionPane.showMessageDialog(this, "A quantidade informada Ã© maior que a quantidade disponivel.");
                this.produto = null;
                this.jButton3.setEnabled(false);
            } else {
                    qtd = (qtds);
                    this.produto.setQtdVenda(qtds);
                    this.setVisible(false);
                    this.dispose();
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Quantidade inserida invalida -"  );
        }
    }//GEN-LAST:event_jButton3ActionPerformed

