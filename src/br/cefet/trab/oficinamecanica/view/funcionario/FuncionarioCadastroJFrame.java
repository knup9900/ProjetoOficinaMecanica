    private void preencherJTable(List<Funcionario> sList) {
        String[] colunas = new String[]{"ID", "Nome", "Endere√ßo", "Tel", "Excluir", "Alterar"};
        String[][] dados = new String[sList.size()][4];
        for (int i = 0; i < sList.size(); i++) {
            Funcionario s = sList.get(i);
            dados[i][0] = s.getCdfuncionario() + "";
            dados[i][1] = s.getNmfuncionario();
            dados[i][2] = s.getEndereco() + "";
            dados[i][3] = s.getTel() + "";

        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                dados, colunas
        ));
        try {
            botao = new ButtonColumnFunc(this.jTable1, 4, sList);
            botao2 = new ButtonColumnFunc(this.jTable1, 5, sList);
        } catch (IOException ex) {
            Logger.getLogger(FuncionarioCadastroJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
