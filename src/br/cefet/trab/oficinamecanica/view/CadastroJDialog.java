    private void listaFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaFuncionariosMouseClicked
        FuncionarioDao funcionarioDao = new FuncionarioDao();
        int codigo = fListaux.get(listaFuncionarios.getSelectedIndex()).getCdfuncionario();
        try {
            funcionario = funcionarioDao.consultarPorCod(codigo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar funcion치rio selecionado : " + ex.getMessage());
        }
        nomeLabel.setText(funcionario.getNmfuncionario() + " Selecionado");
    }//GEN-LAST:event_listaFuncionariosMouseClicked

