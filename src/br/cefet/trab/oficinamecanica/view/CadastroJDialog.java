    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String senha = new String(this.senhaField.getPassword());
        String senha2 = new String(this.senhaField2.getPassword());

        if (usuarioField.getText().equalsIgnoreCase("") || senha.equalsIgnoreCase("") || senha2.equalsIgnoreCase("") || funcionario.getNmfuncionario() == null) {
            JOptionPane.showMessageDialog(null, "Selecione um funcion치rio e preencha os campos");
        } else {
            if (senha.equalsIgnoreCase(senha2)) {

                Login login = new Login();
                login.setCdFuncionario(funcionario.getCdfuncionario());
                login.setUsuario(usuarioField.getText());

                login.setSenha(senha);
                LoginDao loginDao = new LoginDao();
                try {
                    int codigo = loginDao.inserirLogin(login);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar o login : " + ex.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso");
                this.setVisible(false);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Senhas diferem");
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

